SELECT c.NOME AS CLIENTE_COM_CARRO
FROM CLIENTE c JOIN VEICULO v ON 
c.ID_CLIENTE = v.ID_CLIENTE
WHERE v.TIPO LIKE "Carro"
ORDER BY c.NOME;

SELECT c.NOME, COUNT(v.ID_VEICULO) AS QUANTIDADE_VEICULOS
FROM CLIENTE c JOIN VEICULO v ON 
c.ID_CLIENTE = v.ID_CLIENTE
GROUP BY c.NOME;

