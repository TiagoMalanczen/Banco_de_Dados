CREATE DATABASE IF NOT EXISTS SISTEMA_ESTACIONAMENTO;
USE SISTEMA_ESTACIONAMENTO;

CREATE TABLE CLIENTE(

    ID_CLIENTE INT AUTO_INCREMENT,
    NOME VARCHAR(200) NOT NULL,
    CPF VARCHAR (15) NOT NULL,
    PRIMARY KEY (ID_CLIENTE)
);

CREATE TABLE VEICULO(

    ID_VEICULO INT AUTO_INCREMENT,
    PLACA VARCHAR(8) NOT NULL,
    TIPO VARCHAR(200) NOT NULL,
    ID_CLIENTE INT NOT NULL,
    PRIMARY KEY (ID_VEICULO),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE) ON UPDATE RESTRICT ON DELETE RESTRICT
);
CREATE TABLE CONTRATO(

    ID_CONTRATO INT AUTO_INCREMENT,
    ID_CLIENTE INT NOT NULL,
    DATA_INICIO DATE NOT NULL,
    VALOR_MENSAL DECIMAL(10, 2),
    PRIMARY KEY(ID_CONTRATO),
    FOREIGN KEY(ID_CLIENTE) REFERENCES CLIENTE(ID_CLIENTE) ON UPDATE RESTRICT ON DELETE RESTRICT
    
);

