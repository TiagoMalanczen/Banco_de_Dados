SELECT p.NOME_PLANO, SUM(p.PRECO) AS VALOR_TOTAL_PLANO
FROM PLANO p JOIN ALUNO a ON
p.ID_PLANO = a.ID_PLANO
GROUP BY p.NOME_PLANO;

SELECT p.NOME_PLANO, COUNT(a.ID_ALUNO) AS TOTAL_ALUNOS
FROM ALUNO a JOIN PLANO p ON
p.ID_PLANO = a.ID_PLANO
GROUP BY p.NOME_PLANO;

SELECT AVG(p.PRECO) AS VALOR_MEDIO
FROM ALUNO a JOIN PLANO p ON
p.ID_PLANO = a.ID_PLANO;