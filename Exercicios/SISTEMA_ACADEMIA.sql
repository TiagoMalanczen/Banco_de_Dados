CREATE DATABASE SISTEMA_ACADEMIA;
USE SISTEMA_ACADEMIA;

CREATE TABLE PLANO (
    ID_PLANO INT AUTO_INCREMENT,
    NOME_PLANO VARCHAR(100),
    PRECO DECIMAL(10,2),
    PRIMARY KEY(ID_PLANO)
);
CREATE TABLE ALUNO(
    ID_ALUNO INT AUTO_INCREMENT,
    NOME_ALUNO VARCHAR(100),
    STATUS VARCHAR(20) DEFAULT "Ativo",
    ID_PLANO INT NOT NULL,
    PRIMARY KEY(ID_ALUNO),
    FOREIGN KEY(ID_PLANO) REFERENCES PLANO(ID_PLANO) ON UPDATE RESTRICT ON DELETE RESTRICT
);

ALTER TABLE PLANO  MODIFY COLUMN NOME_PLANO  VARCHAR(100) NOT NULL;
ALTER TABLE PLANO MODIFY COLUMN PRECO DECIMAL(10,2) NOT NULL;

