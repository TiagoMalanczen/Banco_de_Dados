--==============================SISTEMA DE GESTAO DE PRODUTOS==============================
CREATE DATABASE IF NOT EXISTS SISTEMA_GESTAO_PRODUTOS ;
USE   SISTEMA_GESTAO_PRODUTOS;

CREATE TABLE FORNECEDOR(

    ID_FORNECEDOR INT AUTO_INCREMENT,
    NOME_EMPRESA VARCHAR(100) NOT NULL,
    PAIS_ORIGEM VARCHAR(50) DEFAULT "Brasil",
    PRIMARY KEY(ID_FORNECEDOR)

);

CREATE TABLE PRODUTO(
    ID_PRODUTO INT AUTO_INCREMENT,
    NOME_PRODUTO VARCHAR(100) NOT NULL,
    CATEGORIA VARCHAR(50) NOT NULL,
    PRECO_UNITARIO DECIMAL(10,2) NOT NULL,
    QUANTIDADE_ESTOQUE INT DEFAULT 0,
    ID_FORNECEDOR INT,
    PRIMARY KEY(ID_PRODUTO),
    FOREIGN KEY(ID_FORNECEDOR) REFERENCES FORNECEDOR(ID_FORNECEDOR) ON UPDATE RESTRICT ON DELETE RESTRICT
)

SHOW TABLES;
DESC PRODUTO;
DESC FORNECEDOR;

