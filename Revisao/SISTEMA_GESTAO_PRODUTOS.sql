SELECT p.CATEGORIA, SUM(p.PRECO_UNITARIO * p.QUANTIDADE_ESTOQUE) AS VALOR_TOTAL
FROM PRODUTO p JOIN FORNECEDOR f ON
f.ID_FORNECEDOR = p.ID_FORNECEDOR
GROUP BY p.CATEGORIA;

SELECT f.NOME_EMPRESA, COUNT(p.ID_FORNECEDOR) AS QUANTIDADES_PRODUTO
FROM PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR
GROUP BY f.NOME_EMPRESA;

SELECT f.NOME_EMPRESA, MAX(p.PRECO_UNITARIO) AS VALOR_MAXIMO,
MIN(p.PRECO_UNITARIO) AS VALOR_MINIMO FROM
PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR
GROUP BY f.NOME_EMPRESA;

SELECT  f.NOME_EMPRESA, SUM(p.PRECO_UNITARIO * p.QUANTIDADE_ESTOQUE) AS VALOR_ESTOQUE
FROM PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR 
GROUP BY f.NOME_EMPRESA;

SELECT p.CATEGORIA, SUM(p.QUANTIDADE_ESTOQUE) AS QUANTIDADE_CATEGORIA
FROM PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR
GROUP BY p.CATEGORIA
HAVING SUM(p.QUANTIDADE_ESTOQUE) > 15;


SELECT f.NOME_EMPRESA, AVG(p.PRECO_UNITARIO) AS VALOR_MEDIO
FROM PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR 
WHERE f.PAIS_ORIGEM NOT LIKE 'Brasil'
GROUP BY f.NOME_EMPRESA
HAVING AVG(p.PRECO_UNITARIO) > 100;

SELECT p.CATEGORIA, SUM(p.QUANTIDADE_ESTOQUE) AS ITENS_BARATOS
FROM PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR 
WHERE p.PRECO_UNITARIO < 50.0
GROUP BY p.CATEGORIA
HAVING SUM(p.QUANTIDADE_ESTOQUE) > 30;

SELECT f.NOME_EMPRESA, COUNT(p.ID_PRODUTO) AS FORNECEDORES_ATIVOS
FROM PRODUTO p JOIN FORNECEDOR f ON
p.ID_FORNECEDOR = f.ID_FORNECEDOR 
WHERE p.PRECO_UNITARIO > 10 
GROUP BY f.NOME_EMPRESA 
HAVING COUNT(p.ID_PRODUTO) > 1;